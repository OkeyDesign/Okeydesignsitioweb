export default function Card() {
  const containerRef = useRef<HTMLDivElement>(null);
  const [scale, setScale] = useState(1);

  const designWidth = 333.857;

  useEffect(() => {
    const el = containerRef.current;
    if (!el) return;
    const observer = new ResizeObserver((entries) => {
      const { width } = entries[0].contentRect;
      setScale(width / designWidth);
    });
    observer.observe(el);
    return () => observer.disconnect();
  }, []);

  return (
    <div ref={containerRef} className="bg-[#ede8e6] overflow-clip relative rounded-[12px] size-full" data-name="Card-7">
      <Boton />
      <Frame />
      {/* Illustration - centered horizontally, anchored to bottom */}
      <div
        className="absolute"
        style={{
          width: `${designWidth}px`,
          height: '287px',
          bottom: '-32px',
          left: '50%',
          transformOrigin: 'bottom center',
          transform: `translateX(-50%) scale(${scale})`,
        }}
      >
        <Frame6 />
      </div>
    </div>
  );
}